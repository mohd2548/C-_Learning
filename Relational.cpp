// ******************* Relational Operators*****************
// Always returns boolean value 

#include <iostream>
using namespace std;

int main() {
    cout << (3 < 4) << endl; //Out: 1 ==> True
    cout << (3 > 4) << endl; //Out: 0 ==> False
    cout << (3 == 4) << endl; //Out: 0 ==> False
    cout << (3 > 2) << endl; //Out: 1 ==> True
    cout << (3 >= 2) << endl; //Out: 1 ==> True
    cout << (3 <= 4) << endl; //Out: 1 ==> True
    cout << (3 != 2) << endl; //Out: 1 ==> True
    return 0;

}
